public class Villano {
    private int edad;
    private String nombre;

    public Villano(int e, String n){
        edad = e;
        nombre = n;
    }

    public int getEdad() {
        return edad;
    }

    public String getNombre() {
        return nombre;
    }

    @Override
    public String toString() {
        return "Edad: " + edad + "\nNombre: " + nombre;
    }
}
